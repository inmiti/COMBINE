class BootTableViewController: UITableViewController {
    
    private var subscriptions = Set<AnyCancellable>()
    //ViewModel e inicializador
    private var viewModel: ViewModelBoot
    
    init(viewModel: ViewModelBoot = ViewModelBoot()) {
        self.viewModel = viewModel
        super.init(nibName: nil, bundle: nil)
    }
    
